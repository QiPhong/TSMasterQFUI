#define QDWORD long long

#define QFUSEMSG 2056	//用户使用的消息id从这个开始
#define QMSGDEF_RET -1	//消息默认返回值，建议所有消息返回不要使用


//单选框消息
#define QM_RADIO_ALL_NSELE 1	//同一组内的所有单选框设置为不选中（是给能容纳多个控件的容器控件进行响应，如QWMange、QTable、QGroup，控件收到这个消息是会给本控件内的控件进行发送 QM_RADIO_NSELE）
#define QM_RADIO_NSELE 2		//目标单选框设置为不选中（制作自己的单选框控件时，需控件对给消息进行响应否者无效）



//容器类
/**QHostCtrl有关的
* ============================================================================================================
* 坐标是否在容器控件内最上边QHostCtrl上，是则返回1
* 参数1：高32位为x坐标，低32位为y坐标
* 参数2：对应QHostCtrl的指针(QHostCtrl**)
* 参数3：保留
* ============================================================================================================
*/
#define QM_POINT_IN_QHOSTCTRL 3		//检查坐标是否在容器控件内最上边QHostCtrl上
/**父控件的大小发生改变时，会发送此消息告诉所有的子控件
* ============================================================================================================
* 父控件大小发生改变
* 返回：保留，暂时返回1
* 参数1：高32位为改变后的width，低32位为改变后的height
* 参数2：保留
* 参数3：保留
* ============================================================================================================
*/
#define QM_PARENTCTRL_SIZE_CHANGE 4		//父控件大小发生改变

/**设置窗口大小
* ============================================================================================================
* 设置窗口大小
* 返回：保留，暂时返回1
* 参数1：高32位为改变后的width，低32位为改变后的height
* 参数2：保留
* 参数3：保留
* ============================================================================================================
*/
#define QM_SETWINDOWS_SIZE 5		//设置窗口大小

/**QHost被左键按下,主要是发给UI编辑窗口知道的
* ============================================================================================================
* 返回：保留，暂时返回1
* 参数1：当前QHost的指针
* 参数2：当前QHost的寄宿控件的指针
* 参数3：保留
* ============================================================================================================
*/
#define QM_QHOST_LDOWN 6	

/**容器控件加入控件（非布局，顺序容器）
* ============================================================================================================
* 返回：保留，暂时返回1
* 参数1：要加入的控件指针
* 参数2：插入的位置
* 参数3：保留
* ============================================================================================================
*/
#define QM_ATTRACH_CTRL 7

/**布局容器如入控件
* ============================================================================================================
* 返回：保留，暂时返回1
* 参数1：要加入的控件指针
* 参数2：左上角位置
* 参数3：右下角位置
* ============================================================================================================
*/
#define QM_INSERT_CTRL 8
/**QHost被右键按下,主要是发给UI编辑窗口知道的
* ============================================================================================================
* 返回：保留，暂时返回1
* 参数1：当前QHost的寄宿控件的指针
* 参数2：左上角位置
* 参数3：右下角位置
* ============================================================================================================
*/
#define QM_QHOST_RDOWN 9	

/**容器类型触发放置控件消息
* ============================================================================================================
* 返回：保留，暂时返回1
* 参数1：要加入的控件的指针
* 参数2：如果是布局对象加入控件，则这个参数为权重
* 参数3：保留
* ============================================================================================================
*/
#define QM_PLACE_CTRL 10

/**取控件的子控件
* ============================================================================================================
* 返回：有则返回子控件数，小于1则为无子控件
* 参数1：空则参数2忽略，非空则用于返回控件指针,二级指针即QWND**
* 参数2：要将取出的子控件索引，0~n
* 参数3：返回控件的寄生控件指针
* ============================================================================================================
*/
#define QM_GETSUBCTRL 11

/**取控件的属性
* ============================================================================================================
* 返回：属性值
* 参数1：保留
* 参数2：保留
* 参数3：保留针
* ============================================================================================================
*/
#define QM_GETATTRIBUTE 12
/**容器类型触发删除控件消息
* ============================================================================================================
* 返回：保留，暂时返回1
* 参数1：要移除的控件指针
* 参数2：保留
* 参数3：保留
* ============================================================================================================
*/
#define QM_REMOVE_CTRL 13
/**请求刷新
* ============================================================================================================
* 返回：保留，暂时返回1
* 参数1：1为刷新同时转发给父控件，2为同时刷新子控件
* 参数2：保留
* 参数3：保留
* ============================================================================================================
*/
#define QM_UPDATE_CTRL 14


